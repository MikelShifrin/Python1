#Alphabetic Telephone Number Translator
#
#Many companies use telephone numbers like
# 800-GOT-JUNK so the number is easier
#for their customers to remember. On a
#standard telephone, the alphabetic letters
#are mapped to numbers in the following way:
#
# A, B, and C = 2
# D, E, and F = 3
# G, H, and I = 4
# J, K, and L = 5
# M, N, and O = 6
# P, Q, R, and S = 7
# T, U, and V = 8
# W, X, Y, and Z = 9
#
#Write a program that asks the user to enter a 10-character
#telephone number in the format XXX-XXX-XXXX. The application
#should display the telephone number with any alphabetic
#characters that appeared in the original translated to their
#numeric equivalent.
#Ex:
# user writes: 800-GOT-JUNK
# program shows: 800-468-5865 
