for draw in range(1,1000,10):
    
    turtle.forward(draw)
    turtle.right(90)
    turtle.forward(draw)
    turtle.right(90)
    turtle.forward(draw)
    turtle.right(90)
    turtle.forward(draw)
    turtle.right(90)
