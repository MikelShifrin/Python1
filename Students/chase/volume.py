def cube(lenth):
    area=lenth*lenth*lenth
    return area
def rectangle_prism(lenth,whidth,high):
    area=lenth*whidth*high
    return area
def cylinder(radius,high):
    area=radius*radius*3.14*high
    return area
def cone(radius,high):
    area=3.14*radius*radius*high/3
    return area
def sphere(radius):
    area=4/3*3.14*radius*radius*radius
    return area
